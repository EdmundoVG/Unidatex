            connection.setAutoCommit(false);

                preparedStatement.setInt(1, id_student);
                preparedStatement.setString(2, name_student);
                preparedStatement.setString(3, lastname_student);
                preparedStatement.setInt(4, birth_student);
                preparedStatement.setString(5, direction_student);
                preparedStatement.setString(6, phone_student);
                preparedStatement.setInt(7, fk_id_session_student);

