            String lastname_student = jTextField_lastnamestudent.getText();
            int birth_student = Integer.parseInt(jTextField_birthstudent.getText());
            String direction_student = jTextField_directionstudent.getText();
            String phone_student = jTextField_phonestudent.getText();
